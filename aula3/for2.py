#Estrutura de repetição For: percorrer listas
#Lista estrutura de dados composta (armazena diversos valores)
#Variável que possui vários valores

nomes = ['Ivan', 'Gael', 'Mariana', "Isis"]
print(nomes)

#imprime todos os nomes da lista utilizando range
for i in range(4):
    print(nomes[i])

print("\n")

#imprime todos os nomes usando interador da lista 
for nome in nomes:
    print(nome)

